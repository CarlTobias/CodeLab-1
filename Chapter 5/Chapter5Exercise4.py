print("\nExercise 4\n")

river = {
    "Amazon River" : "South America",
    "Mississippi River" : "North America", 
    "Nile River" : "Africa",
    "Danube River" : "Europe",
    "Murray River" : "Australia"
}

print("Dictionary Keys:\n")
for kriver in river.keys():
    print(kriver)
print("\n")

print("Dictionary Values:\n")
for vriver in river.values():
    print(vriver)
print("\n")
